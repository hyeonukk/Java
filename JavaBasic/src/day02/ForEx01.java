package day02;

public class ForEx01 {

	public static void main(String[] args) {
		
		//1~10합계
		
		int sum = 0;
		for(int i=1; i<=10; i++) {
			sum += i;
			
		}
		System.out.println(sum);

		System.out.println("---------------");
		
		
		for(int i=10; i>=1; i--){
			
			
			System.out.println(i);
		}
	
	System.out.println("------");
	
		
	for(int a=1; a<=10; a++) {
		
		System.out.println(a);
		}
	
	
	
	
	}
}
